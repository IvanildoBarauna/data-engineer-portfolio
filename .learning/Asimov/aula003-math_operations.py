N1 = 2
N2 = 4

print(f'A soma de {N1} e {N2} resulta em: {N1 + N2}')
print(f'A subtração de {N1} e {N2} resulta em: {N1 -  N2}')
print(f'A multiplicação de {N1} e {N2} resulta em: {N1 *  N2}')
print(f'A divisão de {N1} e {N2} resulta em: {N1 /  N2}')
print(f'A potência quadrada de {N1} resulta em: {N1 ** 2}')
print(f'A potência quadrada de {N2} resulta em: {N2 ** 2}')
print(f'A raiz quadrada de {N1} resulta em: {N1 ** (1/2)}')
print(f'A raiz quadrada de {N2} resulta em: {N2 ** (1/2)}')

